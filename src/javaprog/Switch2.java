package javaprog;

public class Switch2 {

	public static void main(String[] args) {

		
		char c= '+';
		int a=90; int b=88;
		
		
		switch(c)
		{
		
		
		case '-':
		{
			System.out.println(a-b);
			
		}
		break;
		
		case '/':
		{
			System.out.println(a/b);
			
		}
		break;
		
		case '*':
		{
			
			System.out.println(a*b);
		}	
		break;
		
		case '+':
			
		{
			
			System.out.println(a+b);
		}
		break;
		
		default:
			
		{
			System.out.println("wrong input");
		}
		
		}
		
		
		
		
		
		
		
		
	}

}
